weather = "muggy"

case weather
    when weather = "sunny"
        puts "It's a beautiful day today.  Dress lightly!"
    when weather = "rainy"
        puts "Bring an umbrella, and some rain boots!"
    when weather = "cloudy"
        puts "It just might rain. Bring an umbrella and a light jacket."
    when weather = "snowy"
        puts "It's chilly today.  Wear a hat and a scarf!"
else
    puts "Just wear whatever you want!"
    end
        

        
                